// Imperative vs Declarative
// Imperative code: tells the machine what to do and how to do it
//Declarative code: tells what to do and what should happen. It does not tell the computer how to do things.

// Machine code is very imperative.(where to put the variable in the memory space, then take it out somewhere, then modify)Very descriptive of how to do things. Versus Higher level languages become more declarative. We don't say where it should store the memory. We don't say how to do.

// Examples

for (let i = 0; i < 100; i++) {
    console.log(i)
}  // imperative way

[1, 2, 3, 4].forEach(item => console.log(item)) //declarative way

